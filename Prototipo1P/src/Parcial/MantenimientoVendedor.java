    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
        // TODO add your handling code here:
        if (cboBuscar.getSelectedItem().toString()=="Seleccione una opción" || txtId.getText().trim().isEmpty() || txtNombre.getText().trim().isEmpty()
                || txtTelefono.getText().trim().isEmpty() || txtCorreo.getText().trim().isEmpty()
                || txtEdad.getText().trim().isEmpty() || txtEstado.getText().trim().isEmpty() || txtSueldo.getText().trim().isEmpty() ) {
            JOptionPane.showMessageDialog(this, "NO PUEDE HABER CAMPOS VACIOS","WARNING",JOptionPane.WARNING_MESSAGE);
        } else {
           try {
            String ID = cboBuscar.getSelectedItem().toString();

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/parcial", "root", "Rochi1523");
            PreparedStatement pst = cn.prepareStatement("update vendedores set Idvendedor = ?, nombreVendedor=?, telefonoVendedor=?, correoVendedor=?, estadoVendedor=?, edadVendedor=?, sueldoVendedor=?  where Idvendedor = " + ID);

            pst.setString(1, txtId.getText().trim());
            pst.setString(2, txtNombre.getText().trim());
            pst.setString(3, txtTelefono.getText().trim());
            pst.setString(4, txtCorreo.getText().trim());
            pst.setString(5, txtEstado.getText().trim());
            pst.setString(6, txtEdad.getText().trim());
            pst.setString(7, txtSueldo.getText().trim());
            pst.executeUpdate();

            txtId.setText("");
            txtNombre.setText("");
            txtTelefono.setText("");
            txtCorreo.setText("");
            txtEstado.setText("");
            txtEdad.setText("");
            txtSueldo.setText("");
            JOptionPane.showMessageDialog(this, "Modificación exitosa.", "Notificación", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en modificación.", "Error", JOptionPane.ERROR_MESSAGE);
        }
         }
    }//GEN-LAST:event_btnModificarMouseClicked

