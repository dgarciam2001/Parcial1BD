    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
         if (cboBuscar.getSelectedItem().toString()=="Seleccione una opción") {
            JOptionPane.showMessageDialog(this, "NO SE INGRESO NINGUN MAESTRO DE BUSQUEDA");
        } else {
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/parcial", "root", "Rochi1523");
            PreparedStatement pst = cn.prepareStatement("select * from vendedores where Idvendedor = ?");
            pst.setString(1, cboBuscar.getSelectedItem().toString());

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txtId.setText(rs.getString("Idvendedor"));
                txtNombre.setText(rs.getString("nombreVendedor"));
                txtTelefono.setText(rs.getString("telefonoVendedor"));
                txtCorreo.setText(rs.getString("correoVendedor"));
                txtEstado.setText(rs.getString("estadoVendedor"));
                txtEdad.setText(rs.getString("edadVendedor"));
                txtSueldo.setText(rs.getString("sueldoVendedor"));
                JOptionPane.showMessageDialog(this, "Se encontró el registro.", "Notificación", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Registro no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Registro no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
         }
    }//GEN-LAST:event_btnBuscarMouseClicked

